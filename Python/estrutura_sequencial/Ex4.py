nota_um = float(input("Por favor, informe a primeira nota:"))
nota_dois = float(input("\nPor favor, informe a segunda nota:"))
nota_tres = float(input("\nPor favor, informe a terceira nota:"))
nota_quatro = float(input("\nPor favor, informe a quarta nota:"))

media = (nota_um + nota_dois + nota_tres + nota_quatro) / 4
print(f"\nA media das notas informadas foi {media}")
